package com.projectwork.bean;

public class BeveragesDetailsBean
{
    int teaPrice;
    
    int coffeePrice;
    
    int bottlePrice;
    
    int coldDrinkPrice;

    public int getTeaPrice()
    {
        return teaPrice;
    }

    public void setTeaPrice(int teaPrice)
    {
        this.teaPrice = teaPrice;
    }

    public int getCoffeePrice()
    {
        return coffeePrice;
    }

    public void setCoffeePrice(int coffeePrice)
    {
        this.coffeePrice = coffeePrice;
    }

    public int getBottlePrice()
    {
        return bottlePrice;
    }

    public void setBottlePrice(int bottlePrice)
    {
        this.bottlePrice = bottlePrice;
    }

    public int getColdDrinkPrice()
    {
        return coldDrinkPrice;
    }

    public void setColdDrinkPrice(int coldDrinkPrice)
    {
        this.coldDrinkPrice = coldDrinkPrice;
    }

}
