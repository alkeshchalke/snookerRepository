package com.projectwork.dto;

public class CustomerDTO
{
    String customerID;
    
    String customerName;

    String customerFirstName;

    String customerLastName;

    String customerDobDate;

    String customerDobMonth;

    String customerDobYear;

    String customerContactNumber;

    public String getCustomerID()
    {
        return customerID;
    }

    public void setCustomerID(String customerID)
    {
        this.customerID = customerID;
    }

    public String getCustomerName()
    {
        return customerName;
    }

    public void setCustomerName(String customerName)
    {
        this.customerName = customerName;
    }

    public String getCustomerFirstName()
    {
        return customerFirstName;
    }

    public void setCustomerFirstName(String customerFirstName)
    {
        this.customerFirstName = customerFirstName;
    }

    public String getCustomerLastName()
    {
        return customerLastName;
    }

    public void setCustomerLastName(String customerLastName)
    {
        this.customerLastName = customerLastName;
    }

    public String getCustomerDobDate()
    {
        return customerDobDate;
    }

    public void setCustomerDobDate(String customerDobDate)
    {
        this.customerDobDate = customerDobDate;
    }

    public String getCustomerDobMonth()
    {
        return customerDobMonth;
    }

    public void setCustomerDobMonth(String customerDobMonth)
    {
        this.customerDobMonth = customerDobMonth;
    }

    public String getCustomerDobYear()
    {
        return customerDobYear;
    }

    public void setCustomerDobYear(String customerDobYear)
    {
        this.customerDobYear = customerDobYear;
    }

    public String getCustomerContactNumber()
    {
        return customerContactNumber;
    }

    public void setCustomerContactNumber(String customerContactNumber)
    {
        this.customerContactNumber = customerContactNumber;
    }

}